## 状态管理

**定义**：状态管理是应用中管理数据变化和 UI 同步的机制，确保数据变更时界面自动更新。

**类比**：就像教室里的通知板——老师在黑板上写通知（更新状态），所有学生抬头看黑板（UI 自动刷新），不需要一个个去通知。

**技术原理**：通过观察者模式或响应式编程，状态变化时触发依赖该状态的组件重新渲染。

> ⚠️ 全局状态和局部状态需要区分管理，避免不必要的重渲染影响性能。

💡 **思考题**：组件内状态和全局共享状态各自的优缺点是什么？什么场景该用哪种？

