  /// 判断用户输入是否应进入教师模式
  bool _isTeacherModeQuery(String message, String? role) {
    final normalized = message.toLowerCase().replaceAll(RegExp(r'\s+'), '');

    // 教师模式触发关键词
    final teacherKeywords = [
      '教师',
      '教学',
      '班级',
      '学情',
      '课堂',
      '大纲',
      '批阅',
      '教学仪表盘',
      '教学反思',
      '待批',
      '下节课',
      '教案',
      '学生管理',
      '课程覆盖',
      '教学进度',
      '教学规划',
    ];

    // 学生模式触发关键词（当同时匹配时，学生优先 — 非教师角色默认学生模式）
    final studentKeywords = [
      '我的状态',
      '我的学习',
      '我的画像',
      '我的能力',
      '知识图谱健康度',
      '能力雷达',
      '成长曲线',
      '学习风格',
      '下一步学什么',
      '我的孪生',
      '我的成长',
    ];

    final hasTeacher = teacherKeywords.any((k) => normalized.contains(k));
    final hasStudent = studentKeywords.any((k) => normalized.contains(k));

    // 同时匹配时，根据角色决定
    if (hasTeacher && hasStudent) {
      return role == 'teacher' || role == 'admin';
    }

    // 只有教师关键词
    if (hasTeacher) return true;

    // 默认：学生模式（仅教师/管理员角色且主动提起教学相关话题才进教师模式）
    return false;
  }
