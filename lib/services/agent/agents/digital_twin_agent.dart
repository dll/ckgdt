  @override
  Future<AgentMessage> handleMessage(
      String userMessage, AgentSession session) async {
    final userId = AuthService().currentUser?.userId;
    final userRole = AuthService().currentUser?.role;

    String enhancedPersona = config.persona;

    // 智能判断用户意图：关键词匹配决定注入哪种画像数据
    final isTeacherQuery = _isTeacherModeQuery(userMessage, userRole);

    if (userId != null) {
      try {
        if (isTeacherQuery) {
          // 注入教师画像数据
          final profile = await TwinService().buildTeacherProfile(userId);
          enhancedPersona = '''${config.persona}

